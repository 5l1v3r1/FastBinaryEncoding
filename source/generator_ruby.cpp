        GenerateFBEFinalModel("Bool", "bool", "1", "false");
        GenerateFBEFinalModel("Byte", "byte", "1", "0");
        GenerateFBEFinalModel("Char", "char", "1", "\"\\0\"");
        GenerateFBEFinalModel("WChar", "wchar", "4", "\"\\0\"");
